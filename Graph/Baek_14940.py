"""
    @ Baek 14940. 쉬운 최단거리
    @ Prob. https://www.acmicpc.net/problem/14940
     Ref.
    @ Algo: 그래프 (BFS)
    @ Start day: 20. 04. 06.
    @ End day: 20. 04. 06.
"""

from collections import deque

dx = [0, 0, 1, -1]
dy = [-1, 1, 0, 0]

# N: 세로 M: 가로
N, M = map(int, input().split())
MAP = [list(map(int, input().split())) for _ in range(N)]
check = [[-1] * M for _ in range(N)]

q = deque()

for i in range(N):
    for j in range(M):
        if MAP[i][j] == 0:
            check[i][j] = 0
        if MAP[i][j] == 2:
            check[i][j] = 0
            q.append((i, j))

while q:
    x, y = q.popleft()
    for i in range(4):
        nx = x + dx[i]
        ny = y + dy[i]
        if 0 <= nx < N and 0 <= ny < M and check[nx][ny] == -1 and MAP[nx][ny] == 1:
            q.append((nx, ny))
            check[nx][ny] = check[x][y] + 1

for a in check:
    print(' '.join(map(str, a)))


